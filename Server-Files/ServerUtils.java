/**
 * ServerUtils class contains utility methods used by Server Class.
 */
public class ServerUtils {

    public void serverUpNotification() {
        System.out.println("####################################");
        System.out.println("***** Server is Up and Running *****");
        System.out.println("####################################");
    }

    public void serverDownNotification() {
        System.out.println("************************************");
        System.out.println("****** Could not Start Server ******");
        System.out.println("************************************");
    }

}